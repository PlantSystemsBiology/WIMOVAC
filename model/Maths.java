package model;

public class Maths {
	
	public static double DegtoRad(double d) {
	      // This function converts a value in degrees to radians
	      return d*0.01745329;
	}

	public static double RadtoDeg(double d) {
	      // This function converts a value in radians to degrees
	      return d*57.2957795;
	}

}
